// Helper functions
export const getApplicationsByStage = (stage: CandidateStage): JobApplication[] => {
  return mockApplications.filter(app => app.stage === stage);
};

export const getApplicationsByJob = (jobId: string): JobApplication[] => {
  return mockApplications.filter(app => app.jobId === jobId);
};

export const getInterviewsByStatus = (status: Interview["status"]): Interview[] => {
  return mockInterviews.filter(interview => interview.status === status);
};

export const getAssessmentsByStatus = (status: AssessmentAssignment["status"]): AssessmentAssignment[] => {
  return mockAssessmentAssignments.filter(assignment => assignment.status === status);
};

